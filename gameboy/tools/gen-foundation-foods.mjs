// Ports the USDA Foundation Foods dataset into hardcoded C tables for the ROM.
//
// Reads data/examples/usda/FoundationFoods.json and emits, for every food, only the
// fields the tracker needs, all per 100 g:
//   - description (name)
//   - energy (kcal)
//   - protein, total fat, carbohydrate (grams)
//
// Macros are stored as decigrams (0.1 g resolution) in uint16 to keep the table compact
// while preserving one decimal of precision; energy is stored as whole kcal in uint16.
// The generated foundation_foods.{c,h} are committed so the ROM build does not depend on
// the 12 MB source JSON. Re-run with `npm run gb:gen-foods` if the dataset changes.

import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(fileURLToPath(import.meta.url), '..', '..', '..');
const srcJson = join(repoRoot, 'data', 'examples', 'usda', 'FoundationFoods.json');
const outDir = join(repoRoot, 'gameboy', 'src');

// USDA FoodData Central nutrient ids.
const ID_PROTEIN = 1003;
const ID_FAT = 1004;
const ID_CARBS = 1005; // Carbohydrate, by difference (includes fiber, US label convention)
const ID_KCAL = 1008; // Energy (kcal)
const ID_KCAL_ATWATER = [2047, 2048]; // Energy, Atwater General / Specific Factors (kcal)

function amount(food, ...ids) {
    for (const n of food.foodNutrients) {
        if (ids.includes(n.nutrient.id) && typeof n.amount === 'number') {
            return n.amount;
        }
    }
    return null;
}

// Escape a description for a C string literal.
function cString(name) {
    return name.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

const decigrams = (g) => Math.max(0, Math.round((g ?? 0) * 10));

console.log(`Reading ${srcJson} ...`);
const { FoundationFoods } = JSON.parse(readFileSync(srcJson, 'utf8'));

const rows = FoundationFoods.map((food) => {
    const protein = amount(food, ID_PROTEIN) ?? 0;
    const fat = amount(food, ID_FAT) ?? 0;
    const carbs = amount(food, ID_CARBS) ?? 0;
    // Prefer the reported kcal; fall back to Atwater factors, then to a macro estimate.
    let kcal = amount(food, ID_KCAL);
    if (kcal === null) kcal = amount(food, ...ID_KCAL_ATWATER);
    if (kcal === null) kcal = 4 * protein + 9 * fat + 4 * carbs;
    return {
        name: cString(food.description),
        kcal: Math.max(0, Math.round(kcal)),
        protein: decigrams(protein),
        fat: decigrams(fat),
        carbs: decigrams(carbs),
    };
});

const count = rows.length;

const header = `/* Auto-generated by gameboy/tools/gen-foundation-foods.mjs — do not edit by hand. */
/* Source: data/examples/usda/FoundationFoods.json (USDA FoodData Central). */
#ifndef FOUNDATION_FOODS_H
#define FOUNDATION_FOODS_H

#include <stdint.h>

/* One USDA Foundation Food, all values per 100 g.
 * Macros are decigrams (0.1 g resolution); energy is whole kcal. */
typedef struct {
    const char *name;
    uint16_t kcal;        /* energy, kcal */
    uint16_t protein_dg;  /* protein, decigrams (0.1 g) */
    uint16_t fat_dg;      /* total fat, decigrams (0.1 g) */
    uint16_t carbs_dg;    /* carbohydrate by difference, decigrams (0.1 g) */
} foundation_food_t;

#define FOUNDATION_FOOD_COUNT ${count}u

/* The table and its name strings live in this ROM bank. Callers must
 * SWITCH_ROM(FOUNDATION_FOODS_BANK) before dereferencing the array. */
#define FOUNDATION_FOODS_BANK 2

extern const foundation_food_t foundation_foods[FOUNDATION_FOOD_COUNT];

#endif /* FOUNDATION_FOODS_H */
`;

const body = `/* Auto-generated by gameboy/tools/gen-foundation-foods.mjs — do not edit by hand. */
/* Source: data/examples/usda/FoundationFoods.json (USDA FoodData Central). */
#pragma bank 2
#include "foundation_foods.h"

const foundation_food_t foundation_foods[FOUNDATION_FOOD_COUNT] = {
${rows
    .map((r) => `    { "${r.name}", ${r.kcal}u, ${r.protein}u, ${r.fat}u, ${r.carbs}u },`)
    .join('\n')}
};
`;

writeFileSync(join(outDir, 'foundation_foods.h'), header);
writeFileSync(join(outDir, 'foundation_foods.c'), body);

console.log(`Wrote foundation_foods.{c,h} (${count} foods).`);
